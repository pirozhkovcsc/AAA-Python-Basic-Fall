import pytest
from morse import decode


@pytest.mark.parametrize(
    "encoded, expected",
    [
        (
            ".... . .-.. .-.. --- --..--  .-- --- .-. .-.. -.. .-.-.-",
            "HELLO, WORLD."
        ),
        (
            "-- .. ... .... .- -- .. ... .... .- -- .. ... .... .-",
            "MISHAMISHAMISHA"
        ),
        (
            "-....- -....- -....-",
            "---"
        ),
        (
            "-.--.- -.--.- -.--.",
            "))("
        ),
        (
            "..--.. ..--.. ..--.. .-.-.-",
            "???."
        ),
        (
            "-..-. -..-. -..-. --..-- --..-- --..-- --..--",
            "///,,,,"
        ),
        (
            ".--. .-. .. ...- . -",
            "PRIVET"
        ),
        (
            ".- -... -.-. -.. . ..-. --. .... .. .--- -.- .-.. -- -. --- .--. "
            "--.- .-. ... - ..- ...- .-- -..- -.-- --..",
            "ABCDEFGHIJKLMNOPQRSTUVWXYZ"
        ),
        (
            ".?/-(), ",
            ".-.-.- ..--.. -..-. -....- -.--. -.--.- --..--"
        )
    ]
)
def test_decoder(encoded, expected):
    assert decode(encoded) == expected
